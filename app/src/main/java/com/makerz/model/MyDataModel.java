package com.makerz.model;

// Model for MyArrayAdapter and ListMakerZActivity.
public class MyDataModel {

    private String name;
    private String fees;

    // Getter and setters.
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFees() {
        return fees;
    }

    public void setFees(String fees) {
        this.fees = fees;
    }

}